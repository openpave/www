	<p>
		For you, the developer, open source gives you the ability to implement your ideas without
		building from scratch, and to benefit from the changes of other developers. This is
		particularly valuable for students wishing to explore new algorithms, and for researchers
		looking to calibrate models without the expense of developing from scratch.
	</p>

	<p>
		At the moment there is not a lot of code available through OpenPave.org, but it is hoped
		that this will change quickly! OpenPave.org code is not intended to compete with commercial
		offerings, and currently only includes the calculation 'engines', and no user interface
		code.
	</p>

