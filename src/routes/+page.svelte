	<h2>Welcome to Openpave.org</h2>

	<p>
		Welcome to OpenPave.org, the home of open source pavement engineering software. Open source
		software allows you, the user, to access the source code for your software so that you can
		both see how it works and change it to better perform the tasks for which you are using it.
		This is particularly valuable in engineering where you often need specialized software for
		particular tasks. Ordinarily this would mean having to code your application from scratch,
		hence the proliferation of small programs which are essentially the same. With open source
		you can benefit from building on a tested base, and all for free!
	</p>

